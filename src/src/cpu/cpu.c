/* ============================================================================
 * cpu.c — CPU 初始化与复位
 *
 * 本文件只有两个函数：
 *   cpu_init()  — 上电初始化（所有状态归零，特权级 = M）
 *   cpu_reset() — 热复位（等价于 cpu_init）
 *
 * 调用方：simulator.c 的 sim_init()，在模拟器启动时调用一次
 *
 * 依赖：cpu.h（CPU 结构体定义）+ string.h（memset）
 * ============================================================================ */

#include "cpu/cpu.h"
#include <string.h>

/* ── cpu_init ────────────────────────────────────────────────────────
 *
 * CPU 上电初始化：
 *   1. memset 把整个 CPU 结构体清零（regs[32] / pc / CSR / running 全 0）
 *   2. priv = PRIV_MACHINE，模拟器始终以机器态运行
 *
 * 调用后 CPU 处于"就绪但未运行"状态（running = false），
 * 等待 sim_run() 设置 running = true 并进入主循环。
 * ──────────────────────────────────────────────────────────────────── */
void cpu_init(CPU *cpu)
{
    /* 一次性清零：寄存器文件 + PC + CSR + running + priv 全部归零 */
    memset(cpu, 0, sizeof(CPU));

    /* 默认特权级 = 机器态（M-mode），可访问所有内存和 CSR */
    cpu->priv = PRIV_MACHINE;
}

/* ── cpu_reset ───────────────────────────────────────────────────────
 *
 * CPU 热复位：与上电初始化行为一致，直接复用 cpu_init()。
 *
 * 注意：当前版本 init 和 reset 完全相同。后续如果需要区分
 * （例如 reset 保留某些 CSR 值、不清零寄存器等），只需改本函数。
 * ──────────────────────────────────────────────────────────────────── */
void cpu_reset(CPU *cpu)
{
    cpu_init(cpu);
}