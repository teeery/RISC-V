/* ============================================================================
 * cpu.h — CPU 状态结构体定义
 * ============================================================================
 *
 * 这个文件定义 RISC-V CPU 的核心状态。
 *
 * 数据结构：
 *
 *   typedef struct {
 *       uint32_t regs[32];   // 通用寄存器 x0-x31
 *                             // x0 (zero) 硬连线为 0，任何写入都被忽略
 *                             // x1 (ra)   返回地址
 *                             // x2 (sp)   栈指针
 *                             // x3 (gp)   全局指针
 *                             // x4 (tp)   线程指针
 *                             // x5-x7     t0-t2 临时寄存器
 *                             // x8        s0/fp 保存寄存器/帧指针
 *                             // x9        s1 保存寄存器
 *                             // x10-x11   a0-a1 函数参数/返回值
 *                             // x12-x17   a2-a7 函数参数
 *                             // x18-x27   s2-s11 保存寄存器
 *                             // x28-x31   t3-t6 临时寄存器
 *       uint32_t pc;         // 程序计数器（Program Counter）
 *       bool     running;    // true = CPU 正在执行
 *
 *       // ---- 以下为选做扩展 ----
 *       // uint32_t fregs[32]; // 浮点寄存器 f0-f31（F 扩展）
 *       // uint32_t fcsr;      // 浮点控制状态寄存器（F 扩展）
 *       // uint32_t csr[4096]; // CSR 寄存器（特权架构，Zicsr 扩展）
 *   } CPU;
 *
 * 关键规则：
 *   1. regs[0] 永远是 0。每次执行完指令后必须: regs[0] = 0;
 *   2. PC 每次默认 +4（32 位指令固定 4 字节），跳转/分支指令会修改
 *   3. JALR 的目标地址需要 & ~1（最低位清零，对齐到 2 字节边界）
 *
 * 函数声明：
 *   void cpu_init(CPU *cpu);    // 所有 regs=0, pc=0, running=false
 *   void cpu_reset(CPU *cpu);   // 重置 CPU 状态
 */

